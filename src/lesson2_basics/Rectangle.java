package lesson2_basics;

public class Rectangle {
    public static void main(String[] args) {
        int length = 10;
        int width = 33;
        int area = length * width;
        int perimeter = (length + width) *2;
        System.out.println("The length of rectangle id : "+ length);
        System.out.println("The width of rectangle id : "+ width);
        System.out.println("The area of rectangle id : "+area);
        System.out.println("The perimeter of rectangle id : "+ perimeter);
    }
}
