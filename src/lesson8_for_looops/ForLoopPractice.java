package lesson8_for_looops;

public class ForLoopPractice {
    public static void main(String[] args) {
        // number 11-30
        for(int i= 11 ;i<=30; i++){
            System.out.print(i + " ");
        }
        System.out.println();
        // 25 odd number starting from 11
        for(int i=1; i<25; i++){
            System.out.print((9 + i *2) + " ");
        }
        System.out.println();
        // 15 Multiples of 4 staring from 12
        for(int i=0;i<15;i++){
            System.out.print(12+(4*i)+ " ");
        }
        System.out.println();

    }
}
